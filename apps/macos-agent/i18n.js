const SUPPORTED_LOCALES = [
  'en', 'zh', 'zh-Hant', 'ja', 'ko', 'de', 'fr', 'it', 'es', 'pt',
  'hi', 'ar', 'bn', 'id', 'ms', 'th', 'he', 'ru', 'ur', 'tr',
  'vi', 'fa', 'mr', 'ta', 'pl', 'te', 'ne', 'da', 'fi', 'nl', 'no', 'sv',
];

function detectLocale() {
  let sysLocale = 'en';
  try {
    sysLocale = app.getLocale() || 'en';
  } catch (_) {}

  if (SUPPORTED_LOCALES.includes(sysLocale)) return sysLocale;

  const base = sysLocale.split('-')[0];
  if (SUPPORTED_LOCALES.includes(base)) return base;

  if (base === 'zh') {
    if (sysLocale.includes('Hant') || sysLocale.includes('TW') || sysLocale.includes('HK') || sysLocale.includes('MO')) {
      return 'zh-Hant';
    }
    return 'zh';
  }

  return 'en';
}

function buildTranslations(locale) {
  const base = { ...en };
  const langOverrides = overrides[locale];
  if (langOverrides) {
    Object.assign(base, langOverrides);
  }
  return base;
}

let currentLocale = detectLocale();
let translations = buildTranslations(currentLocale);

function t(key) {
  return translations[key] || en[key] || key;
}

function setLocale(locale) {
  if (SUPPORTED_LOCALES.includes(locale)) {
    currentLocale = locale;
    translations = buildTranslations(locale);
  }
}

module.exports = { t, currentLocale, setLocale, SUPPORTED_LOCALES, detectLocale };
